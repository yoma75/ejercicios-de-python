# Los OPERADORES ARITMETICOS se utilizan con valores numéricos para realizar operaciones matemáticas comunes:

x = 5
y = 3

print(x + y)  # 8
print(x - y)  # 2
print(x * y)  # 15
print(x / y)  # 1.6666666666666667


# Modulo: obtener el resto de una división entre dos números
print(x % y)  # 2


# Exponenciación: calcular la potencia de un número
print(x ** y)  # 125


# Division entera: retorna la parte entera del cociente, descartando cualquier parte fraccionaria.
print(x // y)  # 1
