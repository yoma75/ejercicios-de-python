# Los OPERADORES DE COMPARACION se utilizan para comparar dos valores:

# == verificar si dos valores son iguales. 
a = 5
b = 5

if(a == b):
  print('a y b son iguales')
else:
  print('a y b no son iguales')  # a y b no son iguales


# != significa "diferente de" o "no igual a". Se utiliza para comparar dos valores y devolver True si los valores son diferentes, o False 
x = 5
y = 3
print(x != y)  # True


# > verificar si el valor a la izquierda es MAYOR que el valor a la derecha.
# < verificar si el valor a la izquierda es MENOR que el valor a la derecha.
# >= mayor que o igual a
# <= menor que o igual a
x = 78
y = 23

print(x > y)  # True
print(x < y)  # False
print(x >= y)  # True
print(x <= y)  # False
