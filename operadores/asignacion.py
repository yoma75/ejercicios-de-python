# Los OPERADORES DE ASIGNACION se utilizan para asignar valores a las variables:

# = asignacion
x = 5
print(x)  # 5


# +=  suma un valor al valor existente de una variable y luego asigna el resultado a esa misma variable.
x = 5
x += 3
print(x)  # 8


# -=  resta un valor al valor existente de una variable y luego asigna el resultado a esa misma variable.
x = 5
x -= 3
print(x)  # 2


# *=  multiplica un valor al valor existente de una variable y luego asigna el resultado a esa misma variable.
x = 5
x *= 3
print(x)  # 15


# /=  divide un valor al valor existente de una variable y luego asigna el resultado a esa misma variable.
x = 5
x /= 3
print(x)  # 1.6666666666666667


# %=  calcula el módulo con otro valor, y luego asigna el resultado de esa operación de vuelta a la variable.
# obtiene el resto de una división entre dos números
x = 10
x %= 3
print(x)  # 1


# Division entera: retorna la parte entera del cociente, descartando cualquier parte fraccionaria.
x = 6
x //= 2
print(x)  # 3


# Exponenciación: calcular la potencia de un número
x = 5
x **= 3
print(x)  # 125
