# Obtener la documentacion de funciones incorporadas

from math import sin
from datetime import datetime



# Funcion de valor absoluto: abs
print(abs.__doc__) # Return the absolute value of the argument.
print('-'*100)

print(int.__doc__) # Convert a number or string to an integer
print('-'*100)

print(set.__doc__) # Nuevo conjunto
print('_'*100)

print(sin.__doc__) # el seno de una operacion
print('x'*100)

print(datetime.now.__doc__) # Retorna la fecha y hora actual del sistema
