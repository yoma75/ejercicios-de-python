# En el caso de los números, el carácter '+' funciona como un operador matemático:
x = 23
y = 5
print(x + y)  # 28

# -------------------------------------------------------------------------------

# Si se colocara el signo '+' saldria un error, porque no se puede sumar enteros con strings
x = 5
y = "John"
print(x, y)  # 5 John
