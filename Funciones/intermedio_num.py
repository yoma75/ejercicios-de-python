# Realiza una función llamada intermedio(a, b) que a partir de dos números, devuelva su punto intermedio.

def intermedio(a, b):
    return (a+b)//2

print('El numero intermedio es: {}'.format(intermedio(-12, 24)))
print('El numero intermedio es: {}'.format(intermedio(12, 24)))
print('El numero intermedio es: {}'.format(intermedio(-42, 24)))
print('El numero intermedio es: {}'.format(intermedio(65, 24)))
print('El numero intermedio es: {}'.format(intermedio(23, 24)))
print('El numero intermedio es: {}'.format(intermedio(-28, 24)))

