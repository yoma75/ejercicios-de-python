'''min: retorna el elemento menor en un iterable o el menor de dos o más argumentos.'''


numeros = [189, 248, 745, 197, 895, 369]
print(f'El número menor es: {min(numeros)}')  # El número menor es: 189


transport = ['bus', 'avion', 'automovil', 'chiva']
print(f'El primero alfabeticamente es: {min(transport)}')  # automovil

