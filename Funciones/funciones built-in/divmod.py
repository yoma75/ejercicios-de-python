# divmod: devuelve una tupla que contiene el cociente y el resto cuando el argumento 1 (dividendo) es dividido por argumento2 (divisor)

# Muestra el cociente y el resto de 5 dividido por 2 (2 x 2 = 4 a 5, 1)
x = divmod(5, 2)
y = divmod(10, 2)
z = divmod(347, 15)

print(x)  # (2, 1) 
print(y)  # (5, 0) 
print(z)  # (23, 2) 
