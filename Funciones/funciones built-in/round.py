'''round: se utiliza para redondear un número al número de decimales especificado. 
Sintaxis: round(numero, numero_de_decimales)'''

numero = 3.14159
numero_redondeado = round(numero, 2)
print(numero_redondeado)  # 3.14


# Ejemplo:
numero = 3.7
numero_entero = round(numero)
print(numero_entero)  # 4
